    public function getLatestByModuleId(int $moduleId, int $limit = 20): array {
        return $this->createQueryBuilder('d')
            ->andWhere('d.module = :moduleId')
            ->setParameter('moduleId', $moduleId)
            ->orderBy('d.id', 'DESC')
            ->setMaxResults($limit)
            ->getQuery()
            ->getResult()
        ;
